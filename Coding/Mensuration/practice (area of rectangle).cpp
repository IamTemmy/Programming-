//very basic code. Introductory level to finding the area of a rectangle

#include <iostream>
using namespace std;

int main()
{
//length of shape is fixed and given as 4.3 is assigned to it
    double length = 4.3;
//width of shape is fixed and given as 3.2 is assigned to it
    double width = 3.2;
//formula of area of a rectangle which gives the answer by multiplying the assigned numbers to both variables
    cout << length * width; 
}
